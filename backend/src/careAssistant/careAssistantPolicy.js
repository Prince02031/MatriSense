/**
 * Sanitizes and validates the incoming chat history.
 * - Keeps only the last 8 turns
 * - Allows only role='user' or role='assistant'
 * - Trims long content to a maximum of 500 characters
 * - Ignores malformed entries
 */
const sanitizeChatHistory = (chatHistory) => {
  if (!Array.isArray(chatHistory)) return [];

  const sanitized = chatHistory
    .filter(entry => {
      if (!entry || typeof entry !== 'object') return false;
      if (typeof entry.role !== 'string' || typeof entry.content !== 'string') return false;
      const normalizedRole = entry.role.toLowerCase().trim();
      return normalizedRole === 'user' || normalizedRole === 'assistant';
    })
    .map(entry => {
      const normalizedRole = entry.role.toLowerCase().trim() === 'user' ? 'user' : 'assistant';
      let cleanContent = entry.content.trim();
      if (cleanContent.length > 500) {
        cleanContent = cleanContent.substring(0, 500) + '...';
      }
      return {
        role: normalizedRole,
        content: cleanContent
      };
    });

  return sanitized.slice(-8);
};

